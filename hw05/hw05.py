class VendingMachine:
    """A vending machine that vends some product for some price.

    >>> v = VendingMachine('candy', 10)
    >>> v.vend()
    'Machine is out of stock.'
    >>> v.add_funds(15)
    'Machine is out of stock. Here is your $15.'
    >>> v.restock(2)
    'Current candy stock: 2'
    >>> v.vend()
    'You must add $10 more funds.'
    >>> v.add_funds(7)
    'Current balance: $7'
    >>> v.vend()
    'You must add $3 more funds.'
    >>> v.add_funds(5)
    'Current balance: $12'
    >>> v.vend()
    'Here is your candy and $2 change.'
    >>> v.add_funds(10)
    'Current balance: $10'
    >>> v.vend()
    'Here is your candy.'
    >>> v.add_funds(15)
    'Machine is out of stock. Here is your $15.'

    >>> w = VendingMachine('soda', 2)
    >>> w.restock(3)
    'Current soda stock: 3'
    >>> w.restock(3)
    'Current soda stock: 6'
    >>> w.add_funds(2)
    'Current balance: $2'
    >>> w.vend()
    'Here is your soda.'
    """
    "*** YOUR CODE HERE ***"
    def __init__(self, product, price):
        self.product = product
        self.price = price
        self.product_num = 0
        self.balance = 0

    def vend(self):
        if self.product_num == 0 and self.balance == 0:
            return 'Machine is out of stock.'
        elif self.product_num == 0 and self.balance > 0:
            return 'Machine is out of stock.  Here is your ${0}.'.format(self.balance)
        elif self.product_num > 0 and self.balance < self.price:
            return 'You must add ${0} more funds.'.format(self.price - self.balance)
        elif self.product_num > 0 and self.balance == self.price:
            self.balance = 0
            self.product_num -= 1
            return 'Here is your {0}.'.format(self.product)
        elif self.product_num > 0 and self.balance > self.price:
            change = self.balance - self.price
            self.balance = 0
            self.product_num -= 1
            return 'Here is your {0} and ${1} change.'.format(self.product, change)

    def add_funds(self, money):
        if self.product_num == 0:
            return 'Machine is out of stock. Here is your ${0}.'.format(money)
        self.balance += money
        return 'Current balance: ${0}'.format(self.balance)

    def restock(self, stock_num):
        self.product_num += stock_num
        return 'Current {0} stock: {1}'.format(self.product, self.product_num)


def branches(tree):
    return tree[1:]


def label(tree):
    return tree[0]


def is_tree(tree):
    if type(tree) != list or len(tree) < 1:
        return False
    for branch in branches(tree):
        if not is_tree(branch):
            return False
    return True


def Tree(label, branches=[]):
    for branch in branches:
        assert is_tree(branch)
    return [label] + list(branches)


def preorder(t):
    """Return a list of the entries in this tree in the order that they
    would be visited by a preorder traversal (see problem description).

    >>> numbers = Tree(1, [Tree(2), Tree(3, [Tree(4), Tree(5)]), Tree(6, [Tree(7)])])
    >>> preorder(numbers)
    [1, 2, 3, 4, 5, 6, 7]
    >>> preorder(Tree(2, [Tree(4, [Tree(6)])]))
    [2, 4, 6]
    """
    "*** YOUR CODE HERE ***"
    order_list = []
    for i in range(len(t)):
        if type(t[i]) != list:
            order_list.append(t[i])
        elif len(t[i]) == 1:
            order_list.append(t[i][0])
        else:
            mid_list = preorder(t[i])
            order_list += mid_list
    return order_list


def store_digits(n):
    """Stores the digits of a positive number n in a linked list.

    >>> s = store_digits(1)
    >>> s
    Link(1)
    >>> store_digits(2345)
    Link(2, Link(3, Link(4, Link(5))))
    >>> store_digits(876)
    Link(8, Link(7, Link(6)))
    """
    "*** YOUR CODE HERE ***"
    if n < 10:
        return f"Link({n})"
    else:
        m = 10**(len(str(n))-1)
        first = n // m
        last = n % m
        return f"Link({first},{store_digits(last)})"


